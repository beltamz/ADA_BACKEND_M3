//1- El bucle For...of recorre los valores de un array

let languages = ["TypeScript", "JavaScript" , "Python"];

for (let language of languages) {
    console.log(`Ejemplo array: ${language}`);
}

//2- El bucle recorre un string (cadena de texto)

let saludo= "Buenas tardes";

for (let letra of saludo) {
    console.log(`${letra} `);
}